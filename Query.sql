SELECT
    MA_HOA_DON,
    MA_NV_BAN,
    MA_KH,
    TRI_GIA,
    TRI_GIA * PHAN_TRAM_HOA_HONG / 100.0 AS SO_TIEN_HOA_HONG,
    DU_NO
FROM dbo.HoaDon
WHERE NGAY_GIAO_DICH >= '20200101'
  AND NGAY_GIAO_DICH <  '20200401'
  AND (
        TRI_GIA * PHAN_TRAM_HOA_HONG / 100.0 > 50000000
        OR DU_NO > 30000000
      );
